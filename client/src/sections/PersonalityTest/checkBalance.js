const calculatePersonalityPoints = (
    questions
) => {
    const pointsMap = {};

    questions.forEach((question) => {
        question.answers.forEach((answer) => {
            answer.points.forEach((point) => {
                pointsMap[point.personality] =
                    (pointsMap[point.personality] || 0) + point.weight;
            });
        });
    });

    return pointsMap;
};

